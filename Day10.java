public class main {
  public static void main(String[] args) {
        
        /*
        constanta berarti nilai variabel tidak bisa diubah lagi setelah diinisialisasi.
        di deklarasikan dengan kata kuunci final
        */
        
        final double PHI = 3.14;
        
        System.out.println("Nilai Phi : "+PHI);

  }
}
