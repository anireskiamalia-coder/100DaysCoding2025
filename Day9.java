public class main {
  public static void main(String[] args) {
        //deklarasi
        int tb;
        float bb;
        
        //inisialisasi
        tb = 153; 
        bb = 42;    
        
        //menampilkan nilai awal
        System.out.println("Tinggi badan tahun lalu : "+tb);
        System.out.println("Berat badan tahun lalu : "+bb);
        
        //update variable
        tb = 155;
        bb = 43.5f;
        
        //menampilkan nilai setelah update
        
        System.out.println("Tinggi badan tahun lalu : "+tb);
        System.out.println("Berat badan tahun lalu : "+bb);
  }
}
